// Подключаем заголовочный файл нашей библиотеки с объявлением функции search_and_replace
#include "search_replace.h"

// Стандартные библиотеки C для работы с вводом-выводом, памятью, строками и целыми числами
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdint.h>

// Если размер блока не был передан через компилятор (add_definitions), задаем дефолтное значение 4096 байт.
#ifndef BLOCK_SIZE
#define BLOCK_SIZE 4096
#endif

// Вспомогательная функция: преобразует один HEX-символ в его числовое значение.
// Возвращает -1, если переданный символ не является валидным HEX-символом.
static int hex_char_to_val(char c) {
    // Проверяем, является ли символ цифрой от '0' до '9'
    if (c >= '0' && c <= '9') return c - '0';
    // Проверяем, является ли символ строчной буквой от 'a' до 'f'
    if (c >= 'a' && c <= 'f') return c - 'a' + 10;
    // Проверяем, является ли символ заглавной буквой от 'A' до 'F'
    if (c >= 'A' && c <= 'F') return c - 'A' + 10;
    
    // Если ничего не подошло, символ невалидный
    return -1;
}

// Вспомогательная функция: преобразует HEX-строку (например, "dead") в массив байт.
// Параметр out_len используется для возврата длины полученного массива байт через указатель.
// Возвращает NULL, если строка невалидна или произошла ошибка выделения памяти.
static uint8_t *hex_to_bytes(const char *hex, size_t *out_len) {
    // Получаем длину входной строки
    size_t len = strlen(hex);
    
    // HEX-строка должна состоять из пар символов (каждый байт = 2 HEX-символа)
    if (len % 2 != 0) return NULL;

    // Вычисляем длину итогового массива байт
    *out_len = len / 2;
    
    // Выделяем динамическую память под массив байт
    uint8_t *bytes = malloc(*out_len);
    if (!bytes) return NULL; // Проверка на успешность выделения памяти

    // Проходим по строке с шагом 2, преобразуя каждую пару символов в байт
    for (size_t i = 0; i < *out_len; i++) {
        // Преобразуем старший и младший полубайт (ниббл)
        int high = hex_char_to_val(hex[2 * i]);
        int low = hex_char_to_val(hex[2 * i + 1]);
        
        // Если хотя бы один символ невалидный, очищаем память и возвращаем ошибку
        if (high == -1 || low == -1) {
            free(bytes);
            return NULL;
        }
        
        // Собираем полный байт: старший полубайт сдвигаем на 4 бита (умножаем на 16) и складываем с младшим
        bytes[i] = high * 16 + low;
    }
    
    return bytes;
}

// Главная функция программы
int main(int argc, char *argv[]) {
    // Проверяем количество аргументов: имя программы + 4 аргумента (вход, выход, паттерн, замена)
    if (argc != 5) {
        fprintf(stderr, "Использование: %s <входной_файл> <выходной_файл> <hex_паттерн> <hex_замена>\n", argv[0]);
        return 1;
    }

    // Сохраняем пути к файлам в константные указатели для удобства чтения кода и безопасности
    const char *input_path = argv[1];
    const char *output_path = argv[2];

    // Защита от случайной перезаписи: запрещаем использовать один и тот же файл для чтения и записи
    if (strcmp(input_path, output_path) == 0) {
        fprintf(stderr, "Ошибка: входной и выходной файлы не могут совпадать.\n");
        return 1;
    }

    // Преобразуем HEX-строку паттерна для поиска в массив байт
    size_t pattern_len = 0;
    uint8_t *pattern = hex_to_bytes(argv[3], &pattern_len);
    if (!pattern) {
        fprintf(stderr, "Ошибка: некорректная hex-строка для паттерна.\n");
        return 1;
    }

    // Преобразуем HEX-строку замены в массив байт
    size_t replacement_len = 0;
    uint8_t *replacement = hex_to_bytes(argv[4], &replacement_len);
    if (!replacement) {
        fprintf(stderr, "Ошибка: некорректная hex-строка для замены.\n");
        free(pattern); // Не забываем освободить ранее выделенную память при ошибке (защита от утечек)
        return 1;
    }

    // Информируем пользователя о старте и используемом размере блока
    printf("Запуск замены. Размер блока N = %d\n", BLOCK_SIZE);

    // Вызываем основную функцию поиска и замены из нашей библиотеки
    int result = search_and_replace(input_path, output_path,
                                    pattern, pattern_len,
                                    replacement, replacement_len,
                                    BLOCK_SIZE);

    // Освобождаем динамическую память, выделенную под массивы байт паттерна и замены
    free(pattern);
    free(replacement);

    // Проверяем результат выполнения: если не 0, значит произошла ошибка внутри библиотеки
    if (result != 0) {
        fprintf(stderr, "Ошибка при обработке файла.\n");
        return 1;
    }

    // Успешное завершение программы
    printf("Успешно завершено.\n");
    return 0;
}